package com.example.tdd.intro_tdd;

public class Calcolatrice {

	
	
	public int somma (int a,int b) {
		return a+b;
	}

	public int sottrazione(int c,int d) {
		return c-d;
	}
	
	public int moltiplicazione (int e, int f) {
		return e*f;
	}
	
	public int divisione(int g, int h) {
		return g/h;
	}
}
